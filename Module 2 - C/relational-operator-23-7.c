#include <stdio.h>


void main(){

int num1=740;
int num2=356;

// Relational operator / comparison operator

printf("%d == %d : %d",num1,num2,num1==num2); // equality operator, it checks if
// both values are equal or not

printf("\n%d != %d : %d",num1,num2,num1!=num2);

printf("\n%d < %d : %d",num1,num2,num1<num2); // less than
printf("\n%d > %d : %d",num1,num2,num1>num2); // greater than
printf("\n%d <= %d : %d",num1,num2,num1<=num2); // less than or equal to
printf("\n%d >= %d : %d",num1,num2,num1>=num2); // greater than or equal to

}
